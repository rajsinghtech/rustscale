#[test]
fn parse_response_golden() {
    for case in RESPONSE_CASES {
        let (tid, ap) = match parse_response(case.data) {
            Ok(v) => v,
            Err(e) => panic!("{}: parse error: {e:?}", case.name),
        };
        assert_eq!(tid, case.want_tid, "{}: txid", case.name);
        let want = expected_to_socket_addr(&case.want_addr, case.want_port);
        assert_eq!(ap, want, "{}: addr:port", case.name);
    }
}

#[test]
fn is_stun_table() {
    let cases: &[(&[u8], bool)] = &[
        (&[], false),
        (&[0; 20], false),
        // 4 bytes + magic cookie + 11 bytes = 19 (< header): false
        (
            &[
                0x00, 0x00, 0x00, 0x00, 0x21, 0x12, 0xa4, 0x42, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
                0x00, 0x00, 0x00, 0x00, 0x00,
            ],
            false,
        ),
        // 4 bytes + magic cookie + 12 bytes = 20: true
        (
            &[
                0x00, 0x00, 0x00, 0x00, 0x21, 0x12, 0xa4, 0x42, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
                0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
            ],
            true,
        ),
        // 23 bytes (trailing "foo"): true
        (
            &[
                0x00, 0x00, 0x00, 0x00, 0x21, 0x12, 0xa4, 0x42, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
                0x00, 0x00, 0x00, 0x00, 0x00, 0x00, b'f', b'o', b'o',
            ],
            true,
        ),
        // high bits set: false
        (
            &[
                0xf0, 0x00, 0x00, 0x00, 0x21, 0x12, 0xa4, 0x42, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
                0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
            ],
            false,
        ),
        (
            &[
                0x40, 0x00, 0x00, 0x00, 0x21, 0x12, 0xa4, 0x42, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
                0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
            ],
            false,
        ),
        // 0x20 first byte (high bits clear): true
        (
            &[
                0x20, 0x00, 0x00, 0x00, 0x21, 0x12, 0xa4, 0x42, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
                0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
            ],
            true,
        ),
    ];
    for (i, (input, want)) in cases.iter().enumerate() {
        assert_eq!(is_stun(input), *want, "is_stun case {i}");
    }
}

#[test]
fn binding_request_roundtrips() {
    let tx = new_tx_id();
    let req = request(&tx);
    let got_tx = parse_binding_request(&req).expect("parse binding request");
    assert_eq!(got_tx, tx, "txid roundtrips through request/parse");
}

#[test]
fn response_roundtrips_v4_and_v6() {
    let cases: &[(TxID, SocketAddr)] = &[
        (
            [1u8; 12],
            SocketAddr::V4(SocketAddrV4::new(Ipv4Addr::new(1, 2, 3, 4), 254)),
        ),
        (
            [2u8; 12],
            SocketAddr::V4(SocketAddrV4::new(Ipv4Addr::new(1, 2, 3, 4), 257)),
        ),
        (
            [3u8; 12],
            SocketAddr::V6(SocketAddrV6::new("1::4".parse().unwrap(), 254, 0, 0)),
        ),
        (
            [4u8; 12],
            SocketAddr::V6(SocketAddrV6::new("1::4".parse().unwrap(), 257, 0, 0)),
        ),
    ];
    for (tx, addr) in cases {
        let res = response(tx, *addr);
        let (tx2, addr2) = parse_response(&res).expect("parse response");
        assert_eq!(tx2, *tx, "txid matches for {addr}");
        assert_eq!(addr2, *addr, "addr matches for {addr}");
    }
}

#[test]
fn request_attribute_order_for_xdp_derp() {
    // The derp/xdp package assumes the first attribute is SOFTWARE. Keep it so.
    let tx = new_tx_id();
    let req = request(&tx);
    assert!(req.len() >= 20 + 12, "request too short");
    assert_eq!(&req[20..22], &[0x80, 0x22], "first attr is SOFTWARE");
    assert_eq!(&req[24..32], b"tailnode", "SOFTWARE value is \"tailnode\"");
}

#[test]
fn request_has_correct_shape() {
    let tx = new_tx_id();
    let req = request(&tx);
    // Header (20) + SOFTWARE (12) + FINGERPRINT (8) = 40.
    assert_eq!(req.len(), 40);
    assert!(is_stun(&req));
    // Binding request type.
    assert_eq!(req[0], 0x00);
    assert_eq!(req[1], 0x01);
    // Magic cookie + txid in the right place.
    assert_eq!(&req[4..8], &MAGIC_COOKIE);
    assert_eq!(&req[8..20], &tx);
}

fn expected_to_socket_addr(e: &ExpectedAddr, port: u16) -> SocketAddr {
    match e {
        ExpectedAddr::V4(o) => SocketAddr::V4(SocketAddrV4::new(
            Ipv4Addr::new(o[0], o[1], o[2], o[3]),
            port,
        )),
        ExpectedAddr::V6(s) => SocketAddr::V6(SocketAddrV6::new(s.parse().unwrap(), port, 0, 0)),
    }
}
